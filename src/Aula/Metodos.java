package Aula;

public class Metodos {
	public double adição(double a, double b){
		double total = a + b;
		return total;
		
	}
	public double subtração(double a, double b){
		double total = a - b;
		return total;
	
	}
	public double multiplicação(double a, double b){
		double total = a * b;
		return total;
	}
	public double divisão(double a, double b){
		double total = a / b;
		return total;
	}

}
